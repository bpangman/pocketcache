// Shared computed statistics — single source of truth used by Dashboard,
// Activity, Share, and Settings. Changing values here propagates everywhere.
import { TRANSACTIONS, MONTHLY_DATA, PRIOR_MONTHS_SUM } from './transactions';

// Total months in giving history
export const monthsGiving = MONTHLY_DATA.length; // 6

// Average donation per completed month (excludes in-progress current month)
const completedMonths = MONTHLY_DATA.slice(0, -1);
export const avgPerMonth = completedMonths.length > 0
  ? parseFloat((PRIOR_MONTHS_SUM / completedMonths.length).toFixed(2))
  : 0;

// Month-over-month % change comparing the two most recently COMPLETED months.
// Returns null if there is not enough data.
function computeMomChange() {
  if (completedMonths.length < 2) return null;
  const last = completedMonths[completedMonths.length - 1].donated;
  const prev = completedMonths[completedMonths.length - 2].donated;
  if (prev === 0) return null;
  return parseFloat(((last - prev) / prev * 100).toFixed(1));
}
export const momChange = computeMomChange(); // e.g. +14.0

// Estimated total round-up count across all months.
// Current month uses the actual transaction count; prior months are scaled from it.
export const totalRoundupsCount = Math.round(TRANSACTIONS.length * monthsGiving);

// "Since [Month YYYY]" label for the hero card — derived from MONTHLY_DATA start.
export const sinceLabel = `Since ${MONTHLY_DATA[0].month} ${MONTHLY_DATA[0].year}`;

